    /**
     * Boton negativo
     */
    private void ButtonMasMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonMasMenosActionPerformed
        String op="-+*/^";
        
        if(formula.isEmpty() || formula.charAt(formula.length()-1)!='-'){
            TextAreaFormula.append("-");
            formula+="-";
            this.bandpunto=!bandpunto;
        }else if(!formula.isEmpty() && !op.contains(formula.charAt(formula.length()-1)+"")){
            formula='-'+formula;
            TextAreaFormula.setText(formula);
        }else{
            formula=formula.substring(0, formula.length()-1);
            TextAreaFormula.setText(formula);
        }
    }//GEN-LAST:event_ButtonMasMenosActionPerformed

