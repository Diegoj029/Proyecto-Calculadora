     /**
     * Boton igual
     */
    private void ButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonIgualActionPerformed
        String posfija;
        String op="+-*/^";
        
        if(formula.isEmpty()){
            formula=TextAreaFormula.getText();
        }

            if(Calculadora.validaParentesis(formula)){
                posfija=Calculadora.posFija(formula);
                if(Calculadora.validaPosfija(posfija)){
                    resultado=Calculadora.operaPosfija(posfija);
                    TextFieldRes.setText(resultado+"");
                }else if(!op.contains(formula.charAt(formula.length()-1)+"")){
                    TextFieldRes.setText(formula);
                }else
                    TextFieldRes.setText("SYNTAX ERROR");
            }else{
                TextFieldRes.setText("SYNTAX ERROR");
            }
        
        this.bandpunto=!bandpunto;
    }//GEN-LAST:event_ButtonIgualActionPerformed

